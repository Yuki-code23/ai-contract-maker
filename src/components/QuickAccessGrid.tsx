export default function QuickAccessGrid() {
    const [selectedIds, setSelectedIds] = useState<string[]>([]);
    const [isLoaded, setIsLoaded] = useState(false);

    useEffect(() => {
        const saved = localStorage.getItem('quickAccess');
        if (saved) {
            setSelectedIds(JSON.parse(saved));
        } else {
            // Default: AI Generation only (to match previous behavior) or all?
            // Let's default to all for better discovery
            setSelectedIds(QUICK_ACCESS_ITEMS.map(i => i.id));
        }
        setIsLoaded(true);
    }, []);

    if (!isLoaded) {
        return <div className="h-32 animate-pulse bg-gray-100 dark:bg-gray-800 rounded-2xl"></div>;
    }

    const itemsToShow = QUICK_ACCESS_ITEMS.filter(item => selectedIds.includes(item.id));

    if (itemsToShow.length === 0) {
        return (
            <div className="text-center py-8 text-gray-500 dark:text-gray-400 border border-dashed border-gray-300 dark:border-gray-700 rounded-xl">
                クイックアクセス項目が選択されていません。<br />
                <Link href="/settings" className="text-blue-600 hover:underline">設定</Link>から追加してください。
            </div>
        );
    }

    return (
        <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
            {itemsToShow.map((item) => (
                <Link
                    key={item.id}
                    href={item.path}
                    className={`group relative overflow-hidden rounded-2xl bg-gradient-to-br from-${item.colorFrom} to-${item.colorTo} p-6 text-white shadow-lg transition-transform hover:scale-[1.02]`}
                >
                    <div className="relative z-10">
                        <div className="mb-4 p-3 bg-white/20 rounded-lg w-fit">
                            {ICONS[item.id]}
                        </div>
                        <h3 className="text-2xl font-bold mb-2">{item.label}</h3>
                        <p className={`text-${item.colorFrom.split('-')[0]}-100`}>{item.description}</p>
                    </div>
                    <div className="absolute -right-8 -bottom-8 w-32 h-32 bg-white/10 rounded-full blur-2xl group-hover:bg-white/20 transition-colors"></div>
                </Link>
            ))}
        </div>
    );
}
